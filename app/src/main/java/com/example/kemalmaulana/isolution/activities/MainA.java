    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).
            switch (position) {
                case 0 :
                    KehadiranF kehadiranF = new KehadiranF();
                    return kehadiranF;
                case 1 :
                    NilaiF nilaiF = new NilaiF();
                    return nilaiF;
                case 2 :
                    JadwalF jadwalF = new JadwalF();
                    return jadwalF;
                case 3 :
                    PembayaranF pembayaranF = new PembayaranF();
                    return pembayaranF;
                case 4 :
                    RewardPunishmentF rewardPunishmentF = new RewardPunishmentF();
                    return rewardPunishmentF;
                case 5 :
                    ReportF reportF = new ReportF();
                    return reportF;
                case 6 :
                    KalenderF kalenderF = new KalenderF();
                    return kalenderF;
            }
            return null;
        }

        @Override
        public int getCount() {
            // Show 7 total pages.
            return 7;
        }
    }
